//let,var,const//

//var= variable can be re declared and updated. A global scope variable//
var age=25
var age=50
var age=30
console.log(age)//redclared//

//let = variable cannot be redclared but updated. A block scope variable//
let roll=10
roll=20
roll=30
console.log(roll)//not redeclared but updated//

//const = variable cannot be redclared or updated. A block scope variable.//
const a=20
console.log(a)//not redeclared not updated//

